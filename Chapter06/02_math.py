# Import math module
import math

print('Square root : ', math.sqrt(100)) # 10*10 = 10
print('Power : ', math.pow(2,4)) # 2*2*2*2 = 16
print('Round down : ',math.floor(4.5)) # 4
print('Round up : ',math.ceil(4.5)) # 5
print('Factorial : ', math.factorial(5)) # 5*4*3*2*1 = 120
print('GCD : ', math.gcd(12,18)) # 6

# Constant values
print('Pi : ', math.pi) # 3.141
print('E : ', math.e) # 2.718